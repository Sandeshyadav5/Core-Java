package in.co.rays.basic;

public class SwitchCase {
	public static void main(String[] args) {
		int dial = 1;
		switch (dial) {
		case 1:
			System.out.println("One");
			break;
		case 2:
			System.out.println("two");
			break;
		case 3:
			System.out.println("three");
			break;
		default:
			System.out.println("Not exist");
		}
	}

}
